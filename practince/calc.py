def plus(a=0, b=0):
    print(a + b)


def times(a=0, b=0):
    print(a * b)


def minus(a=0, b=100):
    print(a - b)


def divide(a=1, b=1):
    print(a / b)


def power(a=0, b=0):
    print(a ** b)


plus(2, 3)
minus(23, 20)
times(22, 992)
divide(60, 3)
power(3, 22)

